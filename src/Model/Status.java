/*
 * Clase que define os atributos do status da ocorrencia
 */
package Model;

/**
 *
 * @author Colin
 */
public class Status {
    
    private int id;
    private String status;

    public Status() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
    
    
}

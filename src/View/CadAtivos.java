    public void carregaAtivos(){
        List<Ativos> listaatv = new ArrayList();
        listaatv = daoatv.listaAtivos();

        DefaultTableModel model = (DefaultTableModel) jTableativos.getModel();
        model.setNumRows(0);
        
        for(int i= 0; i<listaatv.size() ; i++){
            atv = new Ativos(); 
            atv = listaatv.get(i); 
            String loc = "";
            System.out.println("Locacao_ativos_loc_id");
            if (atv.getLocacao_ativos_loc_id() != 0){
                loc = "Sim";
            }else{
                loc = "Não";
            }
            
            model.addRow(new Object[]{atv.getId(), atv.getTipo(), atv.getDescricao(), 
                                      atv.getModelo(), atv.getFabricante(), atv.getNum_serie(), atv.getNf(), 
                                      atv.getCnpjforn(), atv.getFornecedor(), loc});
        }
        Ativos tipo;
        tipo = new Ativos();
        List<Ativos> listatipo = new ArrayList();
        listatipo = daoatv.listaTipos();
        jCBtipo.removeAllItems();
        for(int i=0; i < listatipo.size(); i++){
            //System.out.println("Tamanho da lista "+i);
            tipo = new Ativos();
            tipo = listatipo.get(i);
            System.out.println("Tipo = "+tipo.getTipo());
            jCBtipo.addItem(tipo.getTipo());
        }
    }


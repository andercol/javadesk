    private void jBTsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTsalvarActionPerformed
        boolean ret = false;
        if(jTFid.getText().isEmpty()){
            /**Novo Ativo*/
            System.out.println("Novo Ativo -----------");
            DAOfornecedor daoforn = new DAOfornecedor();
            Fornecedor forn = new Fornecedor();
            forn = daoforn.consultaCNPJFornecedor(jFTcnpj.getText());
            
            atv = new Ativos();
            atv.setTipo((String) jCBtipo.getSelectedItem());
            atv.setDescricao(jTFdescricao.getText());
            atv.setModelo(jTFmodelo.getText());
            atv.setFabricante(jTFfabricante.getText());
            atv.setNum_serie(jTFnumserie.getText());
            atv.setNf(jTFnf.getText());
            atv.setFornecedor_for_id(forn.getId());
            atv.setFornecedor_for_cnpj(jFTcnpj.getText());

            ret = daoatv.adicionarAtivo(atv);
            if(ret){
                JOptionPane.showMessageDialog(null, "Ativo cadastrado com sucesso! ");
            }else{
                JOptionPane.showMessageDialog(null, "Falha na gravação do banco de dados! ");
            }
        }else{
            /**Atualização*/
            DAOfornecedor daoforn = new DAOfornecedor();
            Fornecedor forn = new Fornecedor();
            forn = daoforn.consultaCNPJFornecedor(jFTcnpj.getText());
            
            atv = new Ativos();
            atv.setId(Integer.parseInt(jTFid.getText()));
            atv.setTipo((String) jCBtipo.getSelectedItem());
            atv.setDescricao(jTFdescricao.getText());
            atv.setModelo(jTFmodelo.getText());
            atv.setFabricante(jTFfabricante.getText());
            atv.setNum_serie(jTFnumserie.getText());
            atv.setNf(jTFnf.getText());
            atv.setFornecedor_for_id(forn.getId());
            atv.setFornecedor_for_cnpj(forn.getCnpj());

            ret = daoatv.updateAtivo(atv);
            if(ret){
                JOptionPane.showMessageDialog(null, "Ativo atualizado com sucesso! ");
            }else{
                JOptionPane.showMessageDialog(null, "Falha atualização do banco de dados! ");
            }
        }
        Cancelar();clear();
        carregaAtivos();
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jBTsalvarActionPerformed


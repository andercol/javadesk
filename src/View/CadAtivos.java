    private void jBTpesquisacnpjActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTpesquisacnpjActionPerformed
        
        if(jFTcnpj.getText().length()< 18){
            JOptionPane.showMessageDialog(rootPane, "CNPJ inválido! confira o valor digitado e tente novamente.");
        }else{
            try {
                DAOfornecedor daoforn = new DAOfornecedor();
                Fornecedor forn = new Fornecedor();
                forn = daoforn.consultaCNPJFornecedor(jFTcnpj.getText());
                String nome = forn.getNome();
                if(forn.getNome() == null){
                    JOptionPane.showMessageDialog(null, "Fornecedor não cadastrado!"+nome);
                }else{
                    jTFfornecedor.setText(forn.getNome());
                    JOptionPane.showMessageDialog(null, "Fornecedor encontrado!");
                }
            } catch (Exception e) {
                System.out.println("Erro ao consultar Fornecedor. "+e);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jBTpesquisacnpjActionPerformed

